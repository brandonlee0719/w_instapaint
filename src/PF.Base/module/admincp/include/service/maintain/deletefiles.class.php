    /**
     * Return list of files have to delete
     *
     * @return array
     */
    public function getListFiles()
    {
        $aFiles440 = $this->get440();
        $aFiles450 = $this->get450();
        $aFiles451 = $this->get451();
        $aFiles460 = $this->get460();
        $aPluginFiles = [];

        if ($sPlugin = Phpfox_Plugin::get('admincp.service_maintain_delete_files_get_list')) {
            eval($sPlugin);
        }
        $aFiles = array_merge($aFiles440, $aFiles450, $aFiles451, $aFiles460, $aPluginFiles);
        //IMPORTANT: Remember check a file delete in old version, but added on newer version
        foreach ($aFiles as $iKey => $sFiles) {
            if (!file_exists(dirname(PHPFOX_DIR) . PHPFOX_DS . $sFiles)) {
                unset($aFiles[$iKey]);
            }
        }
        return $aFiles;
    }
